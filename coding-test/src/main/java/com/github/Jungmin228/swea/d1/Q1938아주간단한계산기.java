package com.github.Jungmin228.swea.d1;

import java.util.Scanner;

public class Q1938아주간단한계산기 {
    class Solution {
        public /*static*/ void main(String args[]) throws Exception {
            Scanner sc = new Scanner(System.in);
            int T1, T2;
            T1 = sc.nextInt();
            T2 = sc.nextInt();

            System.out.println(T1 + T2);
            System.out.println(T1 - T2);
            System.out.println(T1 * T2);
            System.out.println(T1 / T2);

        }
    }
}
